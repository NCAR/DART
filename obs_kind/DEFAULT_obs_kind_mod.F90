   write(*, *) '-------------- EVALUATE_THESE_OBS_TYPES --------------'

   do i = 1, num_kind_evaluate
      write(logfileunit, *) trim(evaluate_these_obs_types(i))
      write(     *     , *) trim(evaluate_these_obs_types(i))
